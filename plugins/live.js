// plugins/live.js
const { smd } = require('../lib/smd');

smd({
    pattern: "live",
    fromMe: false,
    desc: "Show Live Time of the current time-zone"
}, async (message, commandText) => {
    try {
        const now = new Date();
        const hours = now.getHours();
        const minutes = now.getMinutes().toString().padStart(2, '0');
        const seconds = now.getSeconds().toString().padStart(2, '0');
        const time = `${hours}:${minutes}:${seconds}`;
        const date = now.toLocaleDateString();

        let wish = 'GOOD NIGHT 🌙';
        if(hours >= 0 && hours < 5) wish = 'GOOD EARLY MORNING 🌄';
        else if(hours >=5 && hours < 12) wish = 'GOOD MORNING ⛅';
        else if(hours >=12 && hours < 17) wish = 'GOOD AFTERNOON 🌞';
        else if(hours >=17 && hours < 20) wish = 'GOOD EVENING 🌥';

        const timenow = `
╭────────────────╮
│    *${wish}*
│     *TIME* ⌚ ${time}
│     *DATE* 🎲 ${date}
│   ${global.Config.caption}
╰────────────────╯
`;

        await message.send(timenow);

    } catch(e) {
        console.log('Error in live command:', e);
        await message.send('❌ Error: ' + e.toString());
    }
});
